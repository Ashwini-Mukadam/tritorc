                        <p class="text" style="text-align:justify; margin-left:10%; margin-right:10%;">
                            Generating power to keep the lights on in the modern world requires power plants that can utilize various resources to generate heat and produce electricity. This application would be one amongst many applications where planners use heat exchangers, condensers, boilers, and other process equipment in fields such as Chemicals, Oil and gas, Fertilisers, etc.
                            <span class="dots"> ...</span>
                            <span class="moreText">
                                <br>These vessels use tubes to transfer fluids to reduce or increase their temperature. The transfer of heat as efficiently as possible is the objective for these vessels. These metal tubes suffer constant thermal and mechanical stresses caused due to expansion and contraction. Thus, tubes in these vessels are critical to maintain, repair, and replace as and when necessary as they can suffer from Leakages, Rust, and Sediment Buildup/Deposits.
                                <br>These Tubes require Dedicated tools to service them. Tritorc offers tools that find utility for Tube Expansion, Installation, Removal, and Cleaning and bespoke solutions according to the client’s requirements and their specific industry.
                                <br>
                                <br>To elaborate on each of the functions fulfilled by Tritorc’s Tube Tools
                                <br>Tube Expansion:
                                <br>This process involves reducing the tube’s walls by compressing against the tube sheets and flanges.
                                <br>Tubes are mostly made of several materials such as:
                                <br>-Copper & Alloys
                                <br>-Steel
                                <br>-Stainless steel
                                <br>-Titanium and other exotic materials for high pressure vessel applications
                                <br>
                                <br>A general rule for tube expansion involves the amount of reduction acceptable for the particular tube according to the material:
                                <br>- Copper & Cupro Nickel - 8 to 10 %
                                <br>- Steel, Carbon Steel & Admiralty Brass - 7 to 8 %
                                <br>- Stainless Steel & Titanium - 4 to 5 %
                                <br>
                                <br>There are various types of tube expansion tools or torque rolling devices.
                                <br>The tube expander tool has components such as mandrels, cage with rolls, case assembly with thrust collar.
                                <br>The tube rolling tools practically seal the end of the tube against the tube sheet.
                                <br>
                                <br>Selecting the right tools depends on tubes with varied characteristics that are covered in the vast portfolio of Tritorc's vast Tube Expander Series.
                                <br>- Tube Sheet Thicknesses
                                <br>- Tube Materials
                                <br>- Tube Wall Thicknesses
                                <br>- Inner Diameter
                                <br>
                                <br><b>Tube Installation:</b>
                                <br>Here, the various allied tools required to successfully install a tube are discussed.
                                <br>- <b>Tube Sheet Hole Reamer:</b> The Tube sheet hole might not have the exact dimensions required for the accommodation of the tube. Thus Tube sheet hole reamer tools are used to increase the hole size accurately.
                                <br>- <b>Grooving Tools:</b> The tube sheet holes are grooved on the inside to allow for the expanded tube to fill the recesses. This allows for a stronger tube installation against the tube sheet.
                                <br>- <b>Tube End Facer:</b> If the length of the tube is excessively protruding from one tube sheet hole, then the projecting end must be reduced in length using a tube end facer.
                                <br>- <b>Weld Removal Tool:</b> This is a multipurpose tool which can be used during tube installation or removal. The tube end facer is used to remove the weld metal before old tube removal. The tool can also be used for facing, chamfering (Internal & External) tube diameter of condenser and heat exchanger tubes.
                                <br>- <b>Tube Guides:</b> The tool is inserted in the tube with the brush end. The Conical metal guide then projecting outwards is used to orient the tube as it is inserted through the tube sheet and baffles.
                                <br>- <b>Flaring Tool:</b> When combined with a Chipping hammer, the flaring tool is used to flare the tube ends against the tube sheets.
                                <br>- <b>Tube sheet hole brush:</b> After the tube sheet hole is reamed and grooved, the tube sheet hole brush is used to clean the tube sheet to make sure that no debris remains that damages the tube while expanding it.
                                <br>- <b>Tube Leak Detector:</b> In order to detect leaks within the tube itself, the vacuum tube leak detector is used. The tool operates using a vacuum based system. Here the drop in pressure or unsteady pressure is a sign of leakages.
                                <br>- <b>Tube to Tube Sheet Joint Testing Kit:</b> In order to check if the tube expansion is done correctly, the joint between the tube and tube sheet is inspected. The use of vacuum based leak detection system detects any drop in pressure to indicate a leak.
                                <br>- <b>Tube/ Pipe Bevelling Machines:</b> This in situ machining tool is a portable, lightweight and stable platform used for chamfering, bevelling and weld end preparation of tubes and small diameter pipes.
                                <br>
                                <br><b>Tube removal: </b>
                                <br>There are several failure modes and reasons for a tube in a heat exchanger, condenser or boiler such as the following:
                                <br>- Thermal stresses
                                <br>- Leakages
                                <br>- Cross contamination with feed water
                                <br>- Scale deposit build up
                                <br>- pH level of water
                                <br>
                                <br>Once the tube is corroded or damaged and repair or plugging the leaks is no longer possible, it must be removed. Tube extraction systems working with hydraulic, electric and pneumatic drives are available with Tritorc.
                                <br>
                                <br>Tritorc offers various systems such as the following:
                                <br>- Continuous Tube Pulling Systems: A hydraulic tube pulling system with a reciprocating system, is designed to extract tubes from boilers, heat exchangers, condensers, boilers.
                                <br>- Master Tugger Electric pump and Tube pulling guns: Using an electric pump a hydraulic pump is used to operate a hydraulic tube pulling gun to pull tube stubs that have been cut using an internal tube cutter.
                                <br>- Tube Spinner: This tool is a pneumatic tube removal system that uses compressed air to run an air motor to operate a geared system to extract tubes on mass rapidly.
                                <br>
                                <br>Internal tube cutters are used to cut stubbornly stuck tubes in tube sheets. The internal tube cutter punctures and shears the tube on the inner diameter. There are internal tube cutters come in three types such as the following:
                                <br>- Push type Internal Tube Cutter
                                <br>- Internal Tube cutter for thick walled tubes
                                <br>- One Turn Cutter for thin walled tubes
                                <br>These tube tools summarise the several types of tools and the applications that they serve for tubes in heat exchangers, condensers and boilers.
