                            <span class="moreText">
                                <br>Tritorc offers various systems such as the following:
                                <br>- Continuous Tube Pulling Systems: A hydraulic tube pulling system with a reciprocating system, is designed to extract tubes from boilers, heat exchangers, condensers, boilers.
                                <br>- Master Tugger Electric pump and Tube pulling guns: Using an electric pump a hydraulic pump is used to operate a hydraulic tube pulling gun to pull tube stubs that have been cut using an internal tube cutter.
                                <br>- Tube Spinner: This tool is a pneumatic tube removal system that uses compressed air to run an air motor to operate a geared system to extract tubes on mass rapidly.
                                <br>
                                <br>The Continuous Tube Pulling System has several subsystems working in sync.:
                                <br>- The Hydraulic Pump System: The mobile trolley with the Pneumatic driven hydraulic pump provides consistent power output. Tritorc is amongst the most renowned manufacturers of tube pulling machines.
                                <br>- Tube Pulling Gun: The reciprocating Hydraulic Tube Pulling Gun extracts the tube with the use of pine jenny style tube pullers that lock into the tube ID. The tube is removed steadily without damaging the tube sheet with a locking jaw and collar that clamp and release the tube alternatively. Tritorc conducts Manufacture of tube pulling machine.
                                <br>- Pine Jenny style Tube Pulling Tool: This tool has jagged backward pointed edges that jam and lock themselves into the tube once operated with an impact wrench. On being attached to the tube pulling system’s mandrel, the tube pulling gun can extract the tube completely out of the tube sheet.
                                <br>- Tool Balancer: The Spring based tool Balancer can suspend the tube pulling gun and remove operator fatigue. This also allows the operator to focus on the job at hand.
                                <br>
