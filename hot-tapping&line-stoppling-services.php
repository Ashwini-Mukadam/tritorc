                        <h4>Description</h4><br>
                        <p class="text" style="text-align:justify;">
                            Productivity is critical for maintaining profitability for the large economies of scale plants that, to provide an example: refine crude oil into various petrochemical products.
                            <br>Various pipeline fluid mediums such as steam, flare gas, crude oil, water, etc. require careful handling while the pipelines carrying them are drilled into and the flow is redirected to a conduit and then sent to its original destination.
                            <span class="dots"> ...</span>
                            <span class="moreText">
                                <br>Whether the job involves hot tapping a water pipe or a Oil and gas pipeline, uttermost care is a must to ensure safety and loss of productivity.
                                <br>The main pipeline called a mother pipeline is the one that has the flowing fluid. The pipes, to which the mother pipeline could be connected are called branch pipelines. Tritorc provides hot tapping services with its seasoned crew and industry proven equipment.
                                <br>The purpose of Conducting Hot Tapping and line stoppling would be amongst the following:
                                <br>&nbsp;- To remove a damaged or rusted section of pipes
                                <br>&nbsp;- To create a conduit for the fluid to another pipeline and make a branch connection or avoid a certain area and reconnect to the same pipeline.
                                <br>&nbsp;- Entry points for isolation equipment
                                <br>
                                <br>Monitoring points for insertion of temperature probes.
                                <br>There are numerous factors to be considered before the drilling of a live pipeline can commence. Factors beginning with Pipe Dimensions, Flange Rating, Valve Flange Joint Type, Headroom above the valve, Flange Material, Orientation of the Tapping, Design Pressure, Design Temperature to Design Code, Design Factor, and once the tapping is done; whether the tapped joints are stress relieved and hydro tested.
                                <br>Hot Tapping and Line Stoppling are two separate but co-dependent and critically important operations that are bound by several design codes and safety standards.
                                <br>Hot tapping offers the following advantages:
                                <br>&nbsp;- This operation can be performed without the need for plant shutdown
                                <br>&nbsp;- No need for loss of productivity or supply to other areas
                                <br>&nbsp;- Cost effective pipeline modification and repair
                                <br>&nbsp;- Savings in terms of money, time and effort
                                <br>&nbsp;- High pressure tapping on pipeline with a completely spark free/flame free operation
                                <br>
                                <br>Applications of of Hot Tapping and Line Stoppling:
                                <br>1. Oil & Gas Sector: On Shore, Offshore, GCD and Subsea.
                                <br>2. Petrochemical Industry
                                <br>3. Hydrocarbons:
                                <br>&nbsp;- Crude and Heavy Oils
                                <br>&nbsp;- Light Oils, Diesel, Kerosene, Petroleum, Motor Oils.
                                <br>&nbsp;- Chemicals
                                <br>4. Power Generating Industry:
                                <br>&nbsp;- hot tapping water pipe
                                <br>&nbsp;- steam pipe.
                                <br>&nbsp;- Pharmaceuticals, Food Processing Industry, Malls and other large inhabited structures:
                                <br>&nbsp;- Compressed Air, HVAC.
                                <br>
                                <br>On the Topic of Hot Tapping, The operation conducted must remain completely spark and flame free. The setup rig before being mounted on the pipeline , many times must be demonstrated on a rig before attempting to drill on a live pipeline.
                                <br>The Hot tapping machine typically has the following components:
                                <br>A hot Tapping machine consists of a Telescoping boring bar, mechanically driven advancing cutting tool.
                                <br>The Full bore valve (acting as a control valve) allows the drilling machine to be removed.
                                <br>Connection to existing pipe is made through a full encirclement split Tee, RF pad or weld-o-let (welded or bolted) which contains system pressure and provides a connector outlet.
                                <br>Cutter and pilot with coupon are removed from the existing pipe .
                                <br>The coupon is retained by the pilot drill mechanism.
                                <br>The Hot Tapping Process:
                                <br>Hot tapping a pipe involves several complex logistical processes and coordination with plant and onsite crews and officials, but to explain briefly the process as it occurs in hot tapping pipeline:
                                <br>Step 1: The Hot Tapping Machine is mounted on the tapping valve. preceding this the split Tees / Weld o Lets/ RF pads or saddles are mounted using bolted joints or are welded to the pipe depending on the requirement.
                                <br>Step 2: The Cutter Assembly is advanced till it contacts the parent pipe then the hot tap is started.
                                <br>Step 3: When the Pilot Breaks through; the product fills the void and the air is expelled through the purge valve. The valve is then closed to retain the pressure.
                                <br>Step 4: When the Pilot drill is completely through the rotation is stopped and advanced to allow it to drop the U Wires.
                                <br>Step 5: When the Cutter touches the Pipe, the machine is restarted to complete the Cut.
                                <br>Step 6: When the cut is complete the machine is stopped and cutter is advanced to complete the cut
                                <br>Step 7: The Coupon and cutter are withdrawn with the coupon or cut out is retained by U wires.
                                <br>Step 8: The tapping valve is then closed and the machine can be removed.
